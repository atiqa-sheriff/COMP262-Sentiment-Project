def get_metrics(name, y_true, y_pred):
    report = classification_report(y_true, y_pred, output_dict=True, zero_division=0)
    return {
        "Model": name,
        "Accuracy": round(accuracy_score(y_true, y_pred), 4),
        "Macro_F1": round(f1_score(y_true, y_pred, average="macro"), 4),
        "Positive_Recall": round(report["Positive"]["recall"], 4),
        "Neutral_Recall": round(report["Neutral"]["recall"], 4),
        "Negative_Recall": round(report["Negative"]["recall"], 4)
    }

comparison_df = pd.DataFrame([
    get_metrics("Logistic Regression", y_test, lr_pred),
    get_metrics("SVM", y_test, svm_pred),
    get_metrics("VADER", y_test, vader_pred),
    get_metrics("TextBlob", y_test, textblob_pred)
])

