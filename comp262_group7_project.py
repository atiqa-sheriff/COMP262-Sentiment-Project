summaries = []
for _, row in long_reviews.iterrows():
    text = str(row["reviewText"])[:500]
    prompt = f"Summarize this review in about 50 words:\n{text}\nSummary:"
    try:
        output = generator(prompt, max_new_tokens=80, num_return_sequences=1)[0]["generated_text"]
        summary = output.split("Summary:")[-1].strip()
    except:
        summary = "Summary generation failed for this review."
    summaries.append(summary)

