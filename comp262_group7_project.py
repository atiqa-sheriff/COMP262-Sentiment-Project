product_compare["original_rank"] = product_compare["original_avg_rating"].rank(method="dense", ascending=False)
product_compare["enhanced_rank"] = product_compare["enhanced_avg_rating"].rank(method="dense", ascending=False)
product_compare["rank_change"] = product_compare["original_rank"] - product_compare["enhanced_rank"]
product_compare = product_compare.sort_values(by="rank_change", ascending=False)

print("\nTop 10 products improved after sentiment-based enhancement:")
